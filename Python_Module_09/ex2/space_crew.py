from __future__ import annotations

from datetime import datetime
from enum import Enum
from typing import List

from pydantic import BaseModel, Field, model_validator


class Rank(str, Enum):
    """Allowed crew ranks."""
    cadet = "cadet"
    officer = "officer"
    lieutenant = "lieutenant"
    captain = "captain"
    commander = "commander"


class CrewMember(BaseModel):
    """One crew member with validated fields."""
    member_id: str = Field(
        min_length=3,
        max_length=10,
    )
    name: str = Field(
        min_length=2,
        max_length=50,
    )
    rank: Rank
    age: int = Field(
        ge=18,
        le=80,
    )
    specialization: str = Field(
        min_length=3,
        max_length=30,
    )
    years_experience: int = Field(
        ge=0,
        le=50,
    )
    is_active: bool = True


class SpaceMission(BaseModel):
    """Mission with a list of crew members and safety rules."""
    mission_id: str = Field(
        ...,
        min_length=5,
        max_length=15,
    )
    mission_name: str = Field(
        ...,
        min_length=3,
        max_length=100,
    )
    destination: str = Field(
        ...,
        min_length=3,
        max_length=50,
    )
    launch_date: datetime
    duration_days: int = Field(
        ...,
        ge=1,
        le=3650,
    )
    crew: List[CrewMember] = Field(
        ...,
        min_length=1,
        max_length=12,
    )
    mission_status: str = "planned"
    budget_millions: float = Field(
        ...,
        ge=1.0,
        le=10000.0,
    )

    @model_validator(mode="after")
    def check_mission_rules(self) -> "SpaceMission":
        """Validate mission safety requirements that depend on crew data."""
        if not self.mission_id.startswith("M"):
            raise ValueError("Mission ID must start with 'M'")

        # All crew must be active
        for member in self.crew:
            if not member.is_active:
                raise ValueError("All crew members must be active")

        # Must have at least one Commander or Captain
        has_leader = False
        for member in self.crew:
            if member.rank in (Rank.commander, Rank.captain):
                has_leader = True
                break

        if not has_leader:
            raise ValueError(
                "Mission must have at least one Commander or Captain"
            )

        # Long missions need 50% experienced crew (5+ years)
        if self.duration_days > 365:
            experienced = 0
            for member in self.crew:
                if member.years_experience >= 5:
                    experienced += 1

            if experienced * 2 < len(self.crew):
                raise ValueError(
                    "Long missions (> 365 days) need at least 50% "
                    "experienced crew (5+ years)"
                )

        return self


def print_mission(mission: SpaceMission) -> None:
    """Print mission info in a clear format."""
    print(f"Mission: {mission.mission_name}")
    print(f"ID: {mission.mission_id}")
    print(f"Destination: {mission.destination}")
    print(f"Duration: {mission.duration_days} days")
    print(f"Budget: ${mission.budget_millions}M")
    print(f"Crew size: {len(mission.crew)}")
    print("Crew members:")

    for member in mission.crew:
        print(
            f"- {member.name} ({member.rank.value})"
            f" - {member.specialization}"
        )


